// gen-capabilities — emit each app's engine capability manifest: the board kinds + view
// kinds the engine can render/play, WITH a description + field list for each, derived from
// the live code + co-located catalog metadata (the source of truth). `course:check` loads it
// to VALIDATE a course's `board`/`view.kind`; the metadata also serves as the contributor-
// facing catalog of building blocks (a future gallery renders the `example` props to images).
// See docs/specs/2026-06-13-course-format-design.md. Run on any engine change that adds or
// removes a board/view, or edits its catalog metadata:
//   node scripts/gen-capabilities.mjs            → write apps/<app>/engine.capabilities.json
//   node scripts/gen-capabilities.mjs --check    → fail if a committed manifest is stale
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const root = (p) => fileURLToPath(new URL(p, import.meta.url));

// engine CAPABILITY version (board/view vocabulary), matched against a course's `engine:`
// range. Bump the minor when a board/view is added; the major on a breaking removal.
const VERSION = '1.0';

const sortByKind = (a) => [...a].sort((x, y) => x.kind.localeCompare(y.kind));

// the view kinds the renderer actually handles = the `case '<kind>':` labels in views.jsx.
// (views.jsx is JSX, so we read it as text rather than importing.)
const renderedViews = (appDir) => {
  const src = readFileSync(root(`../${appDir}/src/lessons/views.jsx`), 'utf8');
  return [...src.matchAll(/case '([a-z0-9-]+)':/g)].map((m) => m[1]);
};

// every rendered kind must have catalog metadata and vice-versa — keeps the catalog honest.
const cover = (rendered, meta, label, errs) => {
  const have = new Set(Object.keys(meta));
  const seen = new Set(rendered);
  const missing = [...seen].filter((k) => !have.has(k));
  const extra = [...have].filter((k) => !seen.has(k));
  if (missing.length) errs.push(`${label}: missing catalog metadata for: ${missing.join(', ')}`);
  if (extra.length) errs.push(`${label}: catalog metadata for unknown ${label}: ${extra.join(', ')}`);
};

// ── MATH ──
async function mathCaps(errs) {
  const { questionTypes } = await import(root('../packages/math/src/questionTypes/index.js'));
  const { RENDERER_META } = await import(root('../packages/math/src/questionTypes/rendererMeta.js'));
  const { VIEW_META } = await import(root('../packages/math/src/lessons/viewMeta.js'));
  // math boards = the 43 topic ids (what a course references in `board:`), each engine-
  // generated by one renderer (boardKind). Coverage is at the renderer level.
  const renderers = [...new Set(Object.values(questionTypes).map((p) => p.boardKind))];
  cover(renderers, RENDERER_META, 'renderers', errs);
  cover(renderedViews('packages/math'), VIEW_META, 'views', errs);
  const boards = Object.values(questionTypes).map((p) => ({
    kind: p.id,
    title: p.title,
    renderer: p.boardKind,
    generated: true,
    description: RENDERER_META[p.boardKind]?.description,
  }));
  const views = Object.entries(VIEW_META).map(([kind, m]) => ({ kind, ...m }));
  // math content is engine-generated — no authored content collections.
  return { boards: sortByKind(boards), views: sortByKind(views), content: [] };
}

// ── ENGLISH ──
async function englishCaps(errs) {
  // TOPICS lives in PhonicsQuest.jsx (JSX) → read the keys as text.
  const src = readFileSync(root('../packages/english/src/PhonicsQuest.jsx'), 'utf8');
  const m = src.match(/const TOPICS = \{([^}]*)\}/);
  if (!m) throw new Error('english: could not find TOPICS map in PhonicsQuest.jsx');
  const topicKeys = m[1].split(',').map((s) => s.split(':')[0].trim()).filter(Boolean);
  const { BOARD_META } = await import(root('../packages/english/src/boardMeta.js'));
  const { VIEW_META } = await import(root('../packages/english/src/lessons/viewMeta.js'));
  const { CONTENT_META } = await import(root('../packages/english/src/contentMeta.js'));
  cover(topicKeys, BOARD_META, 'boards', errs);
  cover(renderedViews('packages/english'), VIEW_META, 'views', errs);
  const boards = Object.entries(BOARD_META).map(([kind, m]) => ({ kind, ...m }));
  const views = Object.entries(VIEW_META).map(([kind, m]) => ({ kind, ...m }));
  const content = Object.entries(CONTENT_META).map(([id, m]) => ({ id, ...m }));
  return { boards: sortByKind(boards), views: sortByKind(views), content };
}

const APPS = [
  { dir: 'packages/math', caps: mathCaps },
  { dir: 'packages/english', caps: englishCaps },
];

const check = process.argv.includes('--check');
let drift = 0;
const errors = [];

for (const app of APPS) {
  const { boards, views, content } = await app.caps(errors);
  const text = JSON.stringify({ version: VERSION, boards, views, content }, null, 2) + '\n';
  const out = root(`../${app.dir}/engine.capabilities.json`);
  const rel = path.relative(root('..'), out);
  const counts = `${boards.length} boards, ${views.length} views, ${content.length} content`;
  if (check) {
    const have = existsSync(out) ? readFileSync(out, 'utf8') : null;
    if (have === text) {
      console.log(`✓ ${rel} fresh — v${VERSION}, ${counts}`);
    } else {
      drift++;
      console.error(`✗ ${rel} STALE — run \`npm run course:capabilities\` and commit the result`);
      if (have) {
        const old = JSON.parse(have);
        const kinds = (a) => (a || []).map((x) => (typeof x === 'string' ? x : x.kind));
        const diff = (a, b, label) => {
          const A = kinds(a), B = kinds(b);
          const added = A.filter((x) => !B.includes(x));
          const removed = B.filter((x) => !A.includes(x));
          if (added.length) console.error(`    + ${label} added: ${added.join(', ')}`);
          if (removed.length) console.error(`    - ${label} removed: ${removed.join(', ')}`);
        };
        diff(boards, old.boards, 'boards');
        diff(views, old.views, 'views');
        if (old.version !== VERSION) console.error(`    version: ${old.version} → ${VERSION}`);
        // a metadata-only edit (descriptions/fields) won't show in the kind diff above
        if (kinds(boards).join() === kinds(old.boards).join() && kinds(views).join() === kinds(old.views).join())
          console.error('    (kinds unchanged — descriptions/fields edited; just regenerate)');
      }
    }
  } else {
    writeFileSync(out, text);
    console.log(`${rel} — v${VERSION}, ${counts}`);
  }
}

if (errors.length) {
  console.error('\n✗ capability metadata problems:');
  for (const e of errors) console.error('  • ' + e);
  process.exit(2);
}
if (drift) process.exit(1);
